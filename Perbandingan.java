public class Perbandingan {
    public static void main(String[] args) {
        int nilai = 10;
        int minimumKelulusan = 60;

        //== sama dengan
        System.out.println(nilai == minimumKelulusan);

        //!= Tidak Sama dengan
        System.out.println(nilai != minimumKelulusan);

        //> lebih besar
        System.out.println(nilai > minimumKelulusan);

        //< lebih kecil
        System.out.println(nilai < minimumKelulusan);

        //>= lebih besar sama dengan
        System.out.println(nilai >= minimumKelulusan);

        //<= lebih kecil sama dengan
        System.out.println(nilai <= minimumKelulusan);
    }
}
